const walletChoosingObserver = async() => {
    try {
        const web3Modal = new Web3Modal({
            network: "binance", // replace mainnet to binance
            providerOptions, // required
        });

        provider = await web3Modal.connect();
        const providerWrapper = new ethers.providers.Web3Provider(provider)
        const signer = providerWrapper.getSigner()
        contractInt = await new ethers.Contract(CONTRACT_ADDRESS, ABI, signer)
        tokenContract = new ethers.Contract(tokenAddr, tokenAbi, signer)

		contractNFTStaking = await new ethers.Contract(NFTSTAKING_ADDRESS, contractNFTStakingAbi, signer)
		tokenNFTAirDrop = await new ethers.Contract(NFTAIRDROPTOKEN_ADDRESS, tokenNFTAirDropAbi, signer)

        const accountsChangeObservable = accountsChangeObservableFactory(provider)
        accountsChangeObservable.subscribe(accounts => setPersonalStatisticsEvents(providerWrapper, contractInt, tokenContract, accounts[0]))
        const requestAccountObservable = from(requestAccounts(provider)).pipe(takeUntil(accountsChangeObservable))
        requestAccountObservable.subscribe(accounts => setPersonalStatisticsEvents(providerWrapper, contractInt, tokenContract, accounts[0]))
        setGlobalStatisticsEvents(contractInt)
    }
    catch(Exception) {
        console.log(Exception)
    }
};

